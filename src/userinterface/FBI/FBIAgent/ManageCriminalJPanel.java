    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow >= 0) {
            FBIWorkRequest request = (FBIWorkRequest) workRequestJTable.getValueAt(selectedRow, 0);

            request.setStatus("Processing");

            FBIAgentWorkAreaJPanel fBIAgentWorkAreaJPanel = new FBIAgentWorkAreaJPanel(userProcessContainer, account, organization, enterprise, network, business, request);
            userProcessContainer.add("processWorkRequestJPanel", fBIAgentWorkAreaJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);

        } else {
            JOptionPane.showMessageDialog(null, "Please select a request message to process.");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

