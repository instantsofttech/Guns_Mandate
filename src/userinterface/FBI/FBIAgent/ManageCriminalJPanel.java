    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
        String name = nameJTextField.getText();
        String age = AgeJTextField.getText();
        String address = AddressJTextField.getText();
        
        String id = IDJTextField.getText();
        String contact = ContactJTextField.getText();
        String sex = SexJTextField.getText();

        if (!name.isEmpty() && !age.isEmpty()
            && !address.isEmpty() && !id.isEmpty() && !contact.isEmpty() && !sex.isEmpty()) {
            Criminal criminal = criminalDirectory.addCriminal();
            criminal.setCriminalName(nameJTextField.getText());
            criminal.setcAddress(AddressJTextField.getText());
            criminal.setcAge(age);
            criminal.setCriminalId(id);
            criminal.setcSex(SexJTextField.getText());
            criminal.setcContactNumber(contact);

            createUserJButton.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Customer successfully created!");

        } else {
            JOptionPane.showMessageDialog(null, "Please enter values for empty fields");
        }
        populateTable();
    }//GEN-LAST:event_createUserJButtonActionPerformed
public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();
        
        model.setRowCount(0);
        for (Criminal criminal : business.getCriminalList().getCriminalList()){
       // for (Hospital h : organization.getHospitalDirectory().gethospitalList()){
            
            Object row[] = new Object[6];
            row[0] = criminal; 
            row[1] = criminal.getCriminalId();
            row[2] = criminal.getcSex();
            row[3] = criminal.getcAge();
            row[4] = criminal.getcContactNumber();
            row[5] = criminal.getcAddress();
            model.addRow(row);
        }
    }
